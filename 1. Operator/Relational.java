public class Relational 
{
    public static void main(String args[])
    {
        int a=10,b=5;
        System.out.println("10==5 "+(a==b));
        System.out.println("10!=5 "+(a!=b));
        System.out.println("10<5 "+(a<b));
        System.out.println("10>5 "+(a>b));
        System.out.println("10<=5 "+(a<=b));
        System.out.println("10>=5 "+(a>=b));
    }
}