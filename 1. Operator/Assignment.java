public class Assignment 
{
    public static void main(String args[])
    {
        int a=10,b=5,ans;
        ans=a;
        System.out.println(ans);//10
        ans+=b;
        System.out.println(ans);//15
        ans-=a;
        System.out.println(ans);//5
        ans*=a;
        System.out.println(ans);//50
        ans/=b;
        System.out.println(ans);//10
        ans%=3;
        System.out.println(ans);//1
    }
}